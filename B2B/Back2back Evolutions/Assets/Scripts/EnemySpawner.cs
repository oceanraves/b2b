using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class EnemySpawner : MonoBehaviour
{
    void Start()
    {        
    }
    public void SpawnEnemies(int numberOf)
    {
        int enemy = 0;
        while (enemy < numberOf)
        {
            //Instantiate
            enemy++;
        }
    }
}
